function createServiceClient() {
  const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

  if (!supabaseUrl || !serviceRoleKey) {
    throw new Error("Missing NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY.");
  }

  assertServiceRoleKey(serviceRoleKey);

  return createClient(supabaseUrl, serviceRoleKey, {
    auth: {
      persistSession: false
    }
  });
}

function decodeBase64Url(value) {
  const normalized = value.replace(/-/g, "+").replace(/_/g, "/");
  const padded = normalized.padEnd(normalized.length + ((4 - (normalized.length % 4)) % 4), "=");
  return Buffer.from(padded, "base64").toString("utf8");
}

function assertServiceRoleKey(key) {
  const parts = key.split(".");

  if (parts.length !== 3) {
    return;
  }

  try {
    const payload = JSON.parse(decodeBase64Url(parts[1]));

    if (payload.role !== "service_role") {
      throw new Error(
        "SUPABASE_SERVICE_ROLE_KEY is configured, but it is not a service_role key. Copy the service_role secret key from Supabase Project Settings > API Keys, then redeploy Vercel."
      );
    }
  } catch (error) {
    if (error.message?.includes("not a service_role key")) {
      throw error;
    }

    throw new Error("SUPABASE_SERVICE_ROLE_KEY could not be validated. Please re-copy the service_role key from Supabase.");
  }
}

function escapeCsv(value) {
  if (value === null || value === undefined) {
    return "";
  }

  const text = typeof value === "object" ? JSON.stringify(value) : String(value);
  return `"${text.replaceAll('"', '""')}"`;
}

function escapeXml(value) {
  if (value === null || value === undefined) {
    return "";
  }

  const text = typeof value === "object" ? JSON.stringify(value) : String(value);

  return text
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&apos;");
}

function resolveBackupFormat(value) {
  if (value === "csv") {
    return "csv";
  }

  if (value === "excel" || value === "xls") {
    return "excel";
  }

  return "json";
}

function backupToCsv(payload) {
  const lines = [
    "SISTECH DATABASE BACKUP",
    `Generated At,${escapeCsv(payload.meta.generated_at)}`,
    `Generated By,${escapeCsv(payload.meta.generated_by.full_name || payload.meta.generated_by.email || payload.meta.generated_by.id)}`,
    `Role,${escapeCsv(payload.meta.generated_by.role)}`,
    `Date Range Start,${escapeCsv(payload.meta.filters.start_at || "All")}`,
    `Date Range End,${escapeCsv(payload.meta.filters.end_at || "All")}`,
    `Selected Tables,${escapeCsv(payload.meta.selected_tables.map((table) => table.table).join(", "))}`,
    `Total Tables,${payload.meta.table_count}`,
    `Total Rows,${payload.meta.total_rows}`,
    "",
    "SUMMARY",
    "Table,Label,Rows"
  ];

  payload.summary.forEach((summary) => {
    lines.push([escapeCsv(summary.table), escapeCsv(summary.label), summary.row_count].join(","));
  });

  Object.entries(payload.tables).forEach(([tableName, tablePayload]) => {
    const records = tablePayload.records || [];
    const columns = tablePayload.columns || [];

    lines.push("");
    lines.push(`TABLE: ${tablePayload.label} (${tableName})`);
    lines.push(`Rows,${records.length}`);

    if (!records.length) {
      lines.push("No records");
      return;
    }

    lines.push(columns.map(escapeCsv).join(","));
    records.forEach((row) => {
      lines.push(columns.map((column) => escapeCsv(row?.[column])).join(","));
    });
  });

  return lines.join("\n");
}

function excelCell(value, style = "Cell", options = {}) {
  const isNumber = typeof value === "number" && Number.isFinite(value);
  const type = isNumber ? "Number" : "String";
  const attributes = [
    `ss:StyleID="${style}"`,
    options.mergeAcross ? `ss:MergeAcross="${options.mergeAcross}"` : ""
  ]
    .filter(Boolean)
    .join(" ");

  return `<Cell ${attributes}><Data ss:Type="${type}">${isNumber ? value : escapeXml(value)}</Data></Cell>`;
}

function excelRow(cells) {
  return `<Row>${cells.map((cell) => excelCell(cell.value, cell.style, cell)).join("")}</Row>`;
}

function excelColumns(widths) {
  return widths.map((width) => `<Column ss:AutoFitWidth="0" ss:Width="${width}"/>`).join("");
}

function getExcelColumnWidth(column) {
  const lower = column.toLowerCase();

  if (lower === "id" || lower.endsWith("_id")) {
    return 220;
  }

  if (lower.includes("description") || lower.includes("address") || lower.includes("message") || lower.includes("notes") || lower.includes("metadata")) {
    return 240;
  }

  if (lower.includes("date") || lower.includes("_at")) {
    return 140;
  }

  if (lower.includes("amount") || lower.includes("total") || lower.includes("value")) {
    return 120;
  }

  return 150;
}

function sanitizeWorksheetName(name, usedNames) {
  const cleaned = String(name || "Sheet")
    .replace(/[\\/?*:[\]]/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 31) || "Sheet";

  let sheetName = cleaned;
  let counter = 2;

  while (usedNames.has(sheetName)) {
    const suffix = ` ${counter}`;
    sheetName = `${cleaned.slice(0, 31 - suffix.length)}${suffix}`;
    counter += 1;
  }

  usedNames.add(sheetName);
  return sheetName;
}

function buildSummarySheet(payload, usedNames) {
  const rows = [
    excelRow([{ value: "SISTECH DATABASE BACKUP", style: "Title", mergeAcross: 3 }]),
    excelRow([{ value: "", style: "Cell" }]),
    excelRow([
      { value: "Generated At", style: "MetaLabel" },
      { value: payload.meta.generated_at, style: "MetaValue", mergeAcross: 2 }
    ]),
    excelRow([
      { value: "Generated By", style: "MetaLabel" },
      {
        value: payload.meta.generated_by.full_name || payload.meta.generated_by.email || payload.meta.generated_by.id,
        style: "MetaValue",
        mergeAcross: 2
      }
    ]),
    excelRow([
      { value: "Role", style: "MetaLabel" },
      { value: payload.meta.generated_by.role, style: "MetaValue", mergeAcross: 2 }
    ]),
    excelRow([
      { value: "Date Range Start", style: "MetaLabel" },
      { value: payload.meta.filters.start_at || "All", style: "MetaValue", mergeAcross: 2 }
    ]),
    excelRow([
      { value: "Date Range End", style: "MetaLabel" },
      { value: payload.meta.filters.end_at || "All", style: "MetaValue", mergeAcross: 2 }
    ]),
    excelRow([
      { value: "Total Tables", style: "MetaLabel" },
      { value: payload.meta.table_count, style: "MetaValue" }
    ]),
    excelRow([
      { value: "Total Rows", style: "MetaLabel" },
      { value: payload.meta.total_rows, style: "MetaValue" }
    ]),
    excelRow([{ value: "", style: "Cell" }]),
    excelRow([
      { value: "Table Label", style: "Header" },
      { value: "Database Table", style: "Header" },
      { value: "Group", style: "Header" },
      { value: "Rows", style: "Header" }
    ]),
    ...payload.summary.map((summary) => {
      const selectedTable = payload.meta.selected_tables.find((table) => table.table === summary.table);

      return excelRow([
        { value: summary.label, style: "Cell" },
        { value: summary.table, style: "Cell" },
        { value: selectedTable?.group || "-", style: "Cell" },
        { value: summary.row_count, style: "Number" }
      ]);
    })
  ];

  return `
    <Worksheet ss:Name="${escapeXml(sanitizeWorksheetName("Summary", usedNames))}">
      <Table>${excelColumns([220, 220, 160, 100])}${rows.join("")}</Table>
      <WorksheetOptions xmlns="urn:schemas-microsoft-com:office:excel">
        <FreezePanes/>
        <FrozenNoSplit/>
        <SplitHorizontal>11</SplitHorizontal>
        <TopRowBottomPane>11</TopRowBottomPane>
        <ActivePane>2</ActivePane>
      </WorksheetOptions>
    </Worksheet>`;
}

function buildTableSheet(tableName, tablePayload, payload, usedNames) {
  const records = tablePayload.records || [];
  const columns = tablePayload.columns || [];
  const rows = [
    excelRow([{ value: tablePayload.label, style: "Title", mergeAcross: Math.max(columns.length - 1, 1) }]),
    excelRow([{ value: "", style: "Cell" }]),
    excelRow([
      { value: "Database Table", style: "MetaLabel" },
      { value: tableName, style: "MetaValue", mergeAcross: Math.max(columns.length - 2, 0) }
    ]),
    excelRow([
      { value: "Rows", style: "MetaLabel" },
      { value: records.length, style: "MetaValue" }
    ]),
    excelRow([
      { value: "Generated At", style: "MetaLabel" },
      { value: payload.meta.generated_at, style: "MetaValue", mergeAcross: Math.max(columns.length - 2, 0) }
    ]),
    excelRow([{ value: "", style: "Cell" }])
  ];

  if (!records.length) {
    rows.push(excelRow([{ value: "No records", style: "Empty", mergeAcross: Math.max(columns.length - 1, 1) }]));
  } else {
    rows.push(excelRow(columns.map((column) => ({ value: column, style: "Header" }))));

    records.forEach((record, index) => {
      const rowStyle = index % 2 === 0 ? "Cell" : "CellAlt";
      rows.push(
        excelRow(
          columns.map((column) => ({
            value: record?.[column],
            style: typeof record?.[column] === "number" ? "Number" : rowStyle
          }))
        )
      );
    });
  }

  const widths = columns.length ? columns.map(getExcelColumnWidth) : [220, 220];

  return `
    <Worksheet ss:Name="${escapeXml(sanitizeWorksheetName(tablePayload.label || tableName, usedNames))}">
      <Table>${excelColumns(widths)}${rows.join("")}</Table>
      <WorksheetOptions xmlns="urn:schemas-microsoft-com:office:excel">
        <FreezePanes/>
        <FrozenNoSplit/>
        <SplitHorizontal>7</SplitHorizontal>
        <TopRowBottomPane>7</TopRowBottomPane>
        <ActivePane>2</ActivePane>
      </WorksheetOptions>
    </Worksheet>`;
}

