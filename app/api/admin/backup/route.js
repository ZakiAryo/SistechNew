function parseDateParam(value, label) {
  if (!value) {
    return null;
  }

  const parsed = new Date(value);

  if (Number.isNaN(parsed.getTime())) {
    throw new Error(`${label} is not a valid date/time.`);
  }

  return parsed.toISOString();
}

function resolveRequestedTables(searchParams) {
  const requestedTables = searchParams.get("tables");

  if (!requestedTables || requestedTables === "all") {
    return backupTables;
  }

  const selectedTables = requestedTables
    .split(",")
    .map((table) => table.trim())
    .filter(Boolean);
  const allowedTables = new Set(backupTables);
  const invalidTables = selectedTables.filter((table) => !allowedTables.has(table));

  if (invalidTables.length) {
    throw new Error(`Invalid backup table selected: ${invalidTables.join(", ")}.`);
  }

  if (!selectedTables.length) {
    throw new Error("Select at least one database table to backup.");
  }

  return Array.from(new Set(selectedTables));
}

export async function GET(request) {
  const format = resolveBackupFormat(request.nextUrl.searchParams.get("format"));
  const requestedAt = new Date().toISOString();

  try {
    const startAt = parseDateParam(request.nextUrl.searchParams.get("start_at"), "Start date/time");
    const endAt = parseDateParam(request.nextUrl.searchParams.get("end_at"), "End date/time");

    if (startAt && endAt && new Date(startAt) > new Date(endAt)) {
      return NextResponse.json({ error: "Start date/time must be before end date/time." }, { status: 400 });
    }

    let selectedTables;

    try {
      selectedTables = resolveRequestedTables(request.nextUrl.searchParams);
    } catch (error) {
      return NextResponse.json({ error: error.message }, { status: 400 });
    }

    const sessionClient = await createSupabaseServerClient();
    const { data: userData, error: userError } = await sessionClient.auth.getUser();
    const user = userData?.user;

    if (userError || !user) {
      return NextResponse.json({ error: "Unauthorized. Please login as admin." }, { status: 401 });
    }

    const { data: profile, error: profileError } = await sessionClient
      .from("profiles")
      .select("id, email, full_name, role")
      .eq("id", user.id)
      .single();

    if (profileError || profile?.role !== "admin") {
      return NextResponse.json({ error: "Forbidden. Admin role is required for database backup." }, { status: 403 });
    }

    const serviceClient = createServiceClient();
    const tables = {};
    const summary = [];

    for (const tableName of selectedTables) {
      let query = serviceClient
        .from(tableName)
        .select("*")
        .limit(50000);

      if (startAt) {
        query = query.gte("created_at", startAt);
      }

      if (endAt) {
        query = query.lte("created_at", endAt);
      }

      const { data, error } = await query;

      if (error) {
        throw new Error(`Backup failed while reading ${tableName}: ${error.message}`);
      }

      const records = data || [];
      const columns = Array.from(new Set(records.flatMap((row) => Object.keys(row || {}))));
      const tableSummary = {
        table: tableName,
        label: getBackupTableLabel(tableName),
        row_count: records.length
      };

      summary.push(tableSummary);
      tables[tableName] = {
        ...tableSummary,
        columns,
        records
      };
    }

    const totalRows = summary.reduce((total, table) => total + table.row_count, 0);

    const { error: auditError } = await serviceClient.from("audit_logs").insert({
      user_id: user.id,
      action: "backup_database",
      module: "Administration",
      table_name: "database_backup",
      metadata: {
        format,
        requested_at: requestedAt,
        start_at: startAt,
        end_at: endAt,
        tables: selectedTables,
        table_count: selectedTables.length,
        total_rows: totalRows
      }
    });

    if (auditError) {
      throw new Error(`Backup audit log failed: ${auditError.message}`);
    }

    const payload = {
      meta: {
        app: "SISTECH",
        generated_at: requestedAt,
        generated_by: {
          id: profile.id,
          email: profile.email,
          full_name: profile.full_name,
          role: profile.role
        },
        table_count: selectedTables.length,
        total_rows: totalRows,
        selected_tables: selectedTables.map((tableName) => ({
          table: tableName,
          label: getBackupTableLabel(tableName),
          group: backupTableOptions.find((table) => table.value === tableName)?.group || "Other"
        })),
        filters: {
          start_at: startAt,
          end_at: endAt
        }
      },
      summary,
      tables
    };

    const safeTimestamp = requestedAt.replace(/[:.]/g, "-");
    const scopeName = selectedTables.length === backupTables.length ? "all" : "selected";

    if (format === "csv") {
      return new Response(backupToCsv(payload), {
        headers: {
          "content-type": "text/csv; charset=utf-8",
          "content-disposition": `attachment; filename="sistech-backup-${scopeName}-${safeTimestamp}.csv"`
        }
      });
    }

    if (format === "excel") {
      return new Response(backupToExcel(payload), {
        headers: {
          "content-type": "application/vnd.ms-excel; charset=utf-8",
          "content-disposition": `attachment; filename="sistech-backup-${scopeName}-${safeTimestamp}.xls"`
        }
      });
    }

    return new Response(JSON.stringify(payload, null, 2), {
      headers: {
        "content-type": "application/json; charset=utf-8",
        "content-disposition": `attachment; filename="sistech-backup-${scopeName}-${safeTimestamp}.json"`
      }
    });
  } catch (error) {
    return NextResponse.json({ error: error.message || "Backup failed." }, { status: 500 });
  }
}
